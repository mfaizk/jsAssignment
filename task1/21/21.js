var season = prompt("Enter Season");

if (season == "September" || season == "October" || season == "November") {
  alert("Current Season is Autumn");
}
if (season == "December" || season == "January" || season == "February") {
  alert("Current Season is Winter");
}
if (season == "March" || season == "April" || season == "May") {
  alert("Current Season is Spring");
}
if (season == "June" || season == "July" || season == "August") {
  alert("Current Season is Summer");
}
